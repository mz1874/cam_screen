// ================= Setup =================
void setup()
{
  model = tflite::GetModel(g_model);
  if (model->version() != TFLITE_SCHEMA_VERSION)
  {
    MicroPrintf("Model schema version mismatch: %d != %d", model->version(), TFLITE_SCHEMA_VERSION);
    return;
  }

  resolver.AddConv2D();
  resolver.AddFullyConnected();
  resolver.AddSoftmax();
  resolver.AddMaxPool2D();
  resolver.AddReshape();

  static tflite::MicroInterpreter static_interpreter(
      model, resolver, tensor_arena, kTensorArenaSize);
  interpreter = &static_interpreter;

  if (interpreter->AllocateTensors() != kTfLiteOk)
  {
    MicroPrintf("AllocateTensors() failed");
    return;
  }

  input = interpreter->input(0);
  output = interpreter->output(0);

  initialized = true;
  MicroPrintf("Setup complete. Ready for inference.");
}

// ================= Helper: Quantize =================
int8_t QuantizePixel(uint8_t pixel, float scale, int zero_point)
{
  int32_t q = static_cast<int32_t>(round(pixel / scale) + zero_point);
  if (q < -128)
    q = -128;
  if (q > 127)
    q = 127;
  return static_cast<int8_t>(q);
}

// ================= Loop =================
void loop()
{
  if (!initialized)
    return;

  float input_scale = input->params.scale;
  int input_zero_point = input->params.zero_point;

  // 将手写数字1量化并填入输入
  for (int i = 0; i < 28 * 28; i++)
  {
    input->data.int8[i] = QuantizePixel(digit_one[i], input_scale, input_zero_point);
  }

  // 推理
  if (interpreter->Invoke() != kTfLiteOk)
  {
    MicroPrintf("Inference failed");
    return;
  }

  // 找到最大概率的数字
  int8_t max_score = output->data.int8[0];
  int predicted = 0;
  for (int i = 1; i < 10; i++)
  {
    if (output->data.int8[i] > max_score)
    {
      max_score = output->data.int8[i];
      predicted = i;
    }
  }

  MicroPrintf("Predicted digit: %d (confidence score: %d)", predicted, max_score);

  // 打印所有类别的输出分数
  MicroPrintf("All scores:");
  for (int i = 0; i < 10; i++)
  {
    MicroPrintf("  Digit %d: %d", i, output->data.int8[i]);
  }
}
